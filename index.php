      <h4>CONHEÇA NATAL</h4>

     <p><strong>NATAL – CIDADE DO SOL</strong><br>
Conhecida como a Cidade do Sol, Natal, capital do Rio Grande do Norte, está localizada no Nordeste Brasileiro. A cidade oferece ao visitante belíssimas praias, entre elas, Ponta Negra.</p>
<p>
Ponta Negra fica localizada ao sul da Cidade, e é um dos locais mais animados e frequentados. Lá encontra-se um grande número de hoteis, pousadas, bares, restaurantes e casas de show.
</p>
<p>

<p><strong>ARTESANATO</strong> <br>
Um dos melhores artesanatos produzidos no Nordeste é o do Rio Grande do Norte. E um reflexo da qualidade (e da própria quantidade) das peças confeccionadas por verdadeiros artistas manuais está em Natal, que já dispõe de seis grandes centros de venda de produtos artesanais.</p>
<p>
Entre os produtos que compõem o universo do artesanato potiguar estão os bordados, usados sobretudo para propósitos domésticos, assim como as bijuterias, fabricadas com metal e minerais diversos. As camisetas com fotos de praias do Rio Grande do Norte, porém, é que se destacam nas prateleiras.</p>
<p>
Outros itens facilmente encontrados nos centros de artesanato são redes, tapeçarias, roupas de praia feitas de crochê e objetos de enfeite em couro ou madeira, como barcos em miniatura, peixes, ferramentas indígenas e até santos. Também fazem sucesso em meio ao artesanato as guloseimas regionais, com destaque para as castanhas de caju, doces de frutas e até mesmo pastéis de frutos do mar e tapiocas. Por todos esses motivos, conhecer os mercados de artesanato em Natal é diversão garantida. E sinônimo de boas compras.</p>
<p> 
<strong>GASTRONOMIA</strong> <br>
Natal oferece aos turistas, uma variedade de pratos típicos e sua culinária diversificada, é líder na produção de camarão, que faz parte de muitos pratos da culinária local. Muitos dos pratos típicos de Natal são baseados em peixes e frutos do mar (camarão, lagosta, etc); levam temperos e ingredientes diferentes (por exemplo, leite de coco, azeite de dendê) e vêm acompanhados por feijão verde, macaxeira, etc.</p>
<p>
Alguns outros pratos são tipicamente nordestinos, sem similares nas culinárias do sul.</p>
<p>
<strong>Carne de Sol</strong> <br>
Em séculos anteriores, antes da invenção de geladeiras, o povo do Nordeste desenvolveu uma técnica de salgar a carne, para facilitar sua conservação, dando origem ao que hoje se denomina carne de sol. Todas as partes da vaca podem ser salgadas; nos melhores restaurantes, são servidas as partes nobres, que por isso são muito macias. A carne de sol em geral vem em porções generosas (para três ou quatro pessoas), acompanhada em geral por arroz, feijão verde, macaxeira e farofa ou paçoca. É provavelmente o prato mais servido nos restaurantes de comida típica de Natal.</p>
<p>
<strong>Paçoca</strong> <br>
Paçoca, em Natal, não é o doce de amendoim que se conhece no sul. Paçoca é, basicamente, uma farofa composta de pedaços de carne de sol desfiada e farinha de mandioca (mais temperos, como cebola, alho, etc).</p>
<p>
<strong>Tapioca</strong> <br>
Tapioca é feita à base de goma de farinha de mandioca, prensada e levemente aquecida em chapa. Os habitantes locais (principalmente os de camadas sociais mais humildes) consumiam a tapioca pura, mas os turistas costumam encontrá-las com diversos tipos de recheios, doces e salgados: coco ralado, queijo coalho, carne de sol, frango, etc. Tapioca é encontrada em muitas padarias e lanchonetes, geralmente consumida como lanche.</p>
<p>
<strong>Baião de dois</strong> <br>
É preparado cozinhando-se, na mesma panela, primeiro o feijão e em seguida o arroz. É claro que muitas outras misturas podem ser adicionadas durante o cozimento, como carne de sol, queijo coalho, etc.</p>
<p>
<strong>A galinha cabidela ou galinha ao molho pardo</strong> <br>
A galinha cabidela ou ao molho pardo é o mesmo prato. Um dos mais tradicionais da cozinha brasileira. É feito com é feito com galinha caipira cortada em pedaços e guisada num molho feito com o sangue fresco da própria ave. O molho fica amarronzado e é por isso que se chama de molho pardo. </p>
<p>
